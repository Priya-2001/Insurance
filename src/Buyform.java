    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            String msAccDB = "D:/SE"
                    + "/policy.accdb";
            String dbURL = "jdbc:ucanaccess://"
                    + msAccDB;
            Connection con = DriverManager.getConnection(dbURL);
                String name = jTextField1.getText();
                String age = jTextField2.getText();
                String number = jTextField3.getText();
                String pol = jComboBox1.getSelectedItem().toString();
                String polfor = jComboBox2.getSelectedItem().toString();
                String sum = jTextField4.getText();
                String  type = jComboBox3.getSelectedItem().toString();                
                pst = con.prepareStatement("insert into pol(ID,Name,Age,Phonenumber,Policyname,Policyfor,Suminsured,Premiumtype)values(?,?,?,?,?,?,?,?)");
                pst.setString(1, "1");
                pst.setString(2,name);
                pst.setString(3,age);
                pst.setString(4,number);
                pst.setString(5,pol);
                pst.setString(6,polfor);
                pst.setString(7,sum);
                pst.setString(8,type);
                Integer s = Integer.parseInt(sum);
                if(type.equals("Monthly")){
                    s = s / 12;
                }
                if(type.equals("Quarterly")){
                    s = s / 4;
                }
                if(type.equals("Half yearly")){
                    s = s / 2;
                }
                int row = pst.executeUpdate();
             
                if (row > 0) {
                    System.out.println("A row has been inserted successfully.");
                }
                JOptionPane.showMessageDialog(rootPane,"Your policy has been registered.Kindly pay "+s+" as the first installment before logging out.");
        }
        catch(Exception e){
            System.out.println(e);
        }
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed
